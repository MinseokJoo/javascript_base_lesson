// 불리언 (Boolean)
// true, false
let boolTrue = true;
let boolFalse = false;

console.log(boolTrue);
console.log(typeof boolFalse);

console.log(boolFalse);
console.log(typeof boolFalse);

console.log("-----");

// undefined
// un : not, defined: 정의하다
let x;
console.log(x);

console.log("-----");

// null : 값이 존재하지 않음을 '명시적'으로 나타내는 방법
// null !== undefined
let y = null;
console.log(y);

console.log("-----");

// object (객체) : key - value pair
let person = { name: "Minseok", age: 24 };
console.log(person);
console.log(typeof person);

console.log("-----");

// array (배열)
// 여러 개의 데이터를 순서대로 저장하는 데이터 타입
// 인덱스를 가짐
let numArr = [1, 2, 3];
console.log(numArr);
console.log(typeof numArr);
